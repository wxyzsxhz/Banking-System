    private void setupSixDigitOnlyValidation(JPasswordField jpasswordField, JLabel label) {
        jpasswordField.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                checkSixDigitOnly(jpasswordField, label);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                checkSixDigitOnly(jpasswordField, label);
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                // Plain text components do not fire these events
            }
        });
    }

    private void setupEightDigitOnlyValidation(JTextField textField, JLabel label) {
        textField.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                checkEightDigitOnly(textField, label);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                checkEightDigitOnly(textField, label);
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                // Plain text components do not fire these events
            }
        });
    }

    // Call this method to check if the text contains only digits and update the visibility of the corresponding JLabel
    private void checkSixDigitOnly(JTextField textField, JLabel label) {
        String text = textField.getText();
        boolean isDigit = text.matches("\\d*"); // Check if it's a digit
        boolean hasLength6 = text.length() == 6; // Check if it has a length of 6
        label.setVisible(!(isDigit && hasLength6));
    }

    private void checkEightDigitOnly(JTextField textField, JLabel label) {
        String text = textField.getText();
        boolean isDigit = text.matches("\\d*"); // Check if it's a digit
        boolean hasLength8 = text.length() == 8; // Check if it has a length of 8
        label.setVisible(!(isDigit && hasLength8));
    }

