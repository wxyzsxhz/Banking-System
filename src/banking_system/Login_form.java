    private void jLabel17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel17MouseClicked

        try {
            // Specify the URL of the website you want to link to
            String websiteUrl = "https://www.uit.edu.mm";

            // Create a URI object from the URL string
            URI uri = new URI(websiteUrl);

            // Check if the Desktop class is supported (available on the current platform)
            if (Desktop.isDesktopSupported()) {
                // Get the Desktop instance
                Desktop desktop = Desktop.getDesktop();

                // Open the default web browser and navigate to the specified URL
                desktop.browse(uri);
            } else {
                System.out.println("Desktop not supported. Please open the browser and visit: " + websiteUrl);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLabel17MouseClicked

